// Statement 타입 정의: 질문 텍스트와 어떤 지표에 영향을 주는지 명시합니다.
export interface Statement {
  id: number; // 문항 고유 ID
  statement: string; // 질문 내용
  // 'E', 'I', 'S', 'N', 'T', 'F', 'J', 'P'
  // 'A' (Atlas), 'B' (Blaze)
  // 'C' (Charm), 'D' (Dawn)
  // 각 지표는 해당 질문이 점수에 영향을 미치는 방향을 나타냅니다.
  // 예를 들어, 'E'는 외향성 점수를 높이는 방향, 'I'는 내향성 점수를 높이는 방향입니다.
  // Atlas(A)/Blaze(B)는 감정의 동요 축: A는 안정형, B는 신경형
  // Charm(C)/Dawn(D)는 매력/주도성 축: C는 매력적/주도적, D는 소극적/비매력적
  // 중요: 한 질문이 여러 지표에 영향을 줄 수도 있습니다. (예: 'EN', 'AC')
  // 질문이 특정 지표의 긍정적인 방향이라면 해당 지표를, 부정적인 방향이라면 반대 지표를 넣어 점수를 역전시킵니다.
  // 이 배열의 첫 번째 요소는 질문이 직접적으로 측정하는 지표를,
  // 두 번째 요소부터는 질문이 부가적으로 영향을 줄 수 있는 다른 지표들을 나타냅니다.
  // 예를 들어, '파티에서 에너지를 얻는다'는 'E'에 긍정적이고, 'I'에 부정적이므로 ['E', 'I_reverse'] 로 처리할 수 있지만,
  // 여기서는 단순히 'E'에 해당하는 질문으로만 간주하고,
  // 점수 계산 로직에서 E와 I가 상반되는 점수를 가지도록 처리할 것입니다.
  // 따라서, 각 질문은 해당 지표의 긍정적인 방향에 대한 점수만 부여하도록 설계합니다.
  // 예를 들어 '활동적인 것을 좋아한다' -> 'E'에 +점수.
  // '조용히 혼자 있는 것을 좋아한다' -> 'I'에 +점수.
  // (MBTI는 양 극단 중 어느 쪽에 가까운지를 측정하므로 한 지표만 명시합니다.)
  // (CharMTI는 별도의 지표이므로 A/B, C/D를 각각 명시합니다.)
  type: ('E' | 'I' | 'S' | 'N' | 'T' | 'F' | 'J' | 'P' | 'A' | 'B' | 'C' | 'D')[];
}

